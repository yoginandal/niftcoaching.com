  <script>
    // Set a friendly last-updated date (today)
    const d = new Date();
    document.getElementById('pp-date').textContent = d.toLocaleDateString(undefined, { year: 'numeric', month: 'long', day: 'numeric' });

    // Highlight active section in TOC based on viewport using IntersectionObserver
    const sectionIds = [
      'privacy-statement',
      'information',
      'consent',
      'disclosure',
      'payment',
      'third-party',
      'security',
      'cookies',
      'age-of-consent',
      'changes',
      'contact'
    ];

    const sections = sectionIds
      .map(id => document.getElementById(id))
      .filter(Boolean);
    const tocLinks = Array.from(document.querySelectorAll('.toc-link'));

    function setActive(id) {
      tocLinks.forEach(link => {
        const isActive = link.getAttribute('href') === `#${id}`;
        if (isActive) {
          link.classList.add('text-rose-600');
          link.classList.add('bg-rose-50');
          link.classList.add('border-rose-500');
        } else {
          link.classList.remove('text-rose-600');
          link.classList.remove('bg-rose-50');
          link.classList.remove('border-rose-500');
        }
      });
    }

    const observer = new IntersectionObserver((entries) => {
      // Find the entry closest to the top and intersecting
      const visible = entries
        .filter(e => e.isIntersecting)
        .sort((a, b) => Math.abs(a.boundingClientRect.top) - Math.abs(b.boundingClientRect.top));
      if (visible.length > 0) {
        setActive(visible[0].target.id);
      }
    }, {
      root: null,
      rootMargin: '0px 0px -60% 0px', // triggers when section crosses 40% from top
      threshold: [0.1, 0.25, 0.5]
    });

    sections.forEach(sec => observer.observe(sec));

    // Also update on hash navigation clicks
    tocLinks.forEach(link => {
      link.addEventListener('click', () => {
        const id = link.getAttribute('href').slice(1);
        setTimeout(() => setActive(id), 200);
      });
    });
  </script>
