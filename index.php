        document.addEventListener('DOMContentLoaded', function () {
            // Only initialize slider on mobile
            if (window.innerWidth < 768) {
                initMobileSlider();
            }
            // Re-initialize on resize
            window.addEventListener('resize', function () {
                if (window.innerWidth < 768) {
                    initMobileSlider();
                }
            });
            function initMobileSlider() {
                const slider = document.querySelector('.features-grid_online');
                const slides = document.querySelectorAll('.slider-item');
                const dotsContainer = document.querySelector('.slider-dots');
                // Clear existing dots
                dotsContainer.innerHTML = '';
                // Create dots based on number of slides
                slides.forEach((_, index) => {
                    const dot = document.createElement('div');
                    dot.classList.add('dot');
                    if (index === 0) dot.classList.add('active');
                    dot.addEventListener('click', () => goToSlide(index));
                    dotsContainer.appendChild(dot);
                });
                let currentSlide = 0;
                const dots = document.querySelectorAll('.dot');
                // Auto slide function
                function autoSlide() {
                    currentSlide = (currentSlide + 1) % slides.length;
                    goToSlide(currentSlide);
                }
                // Go to specific slide
                function goToSlide(index) {
                    slider.scrollTo({
                        left: slides[index].offsetLeft - slider.offsetLeft,
                        behavior: 'smooth'
                    });
                    // Update active dot
                    dots.forEach(dot => dot.classList.remove('active'));
                    dots[index].classList.add('active');
                    currentSlide = index;
                }
                // Set up auto sliding
                let slideInterval = setInterval(autoSlide, 4000);
                // Pause auto sliding when user interacts with slider
                slider.addEventListener('touchstart', () => {
                    clearInterval(slideInterval);
                });
                slider.addEventListener('touchend', () => {
                    slideInterval = setInterval(autoSlide, 4000);
                });
                // Update active dot when user manually scrolls
                slider.addEventListener('scroll', function () {
                    const scrollPosition = slider.scrollLeft;
                    slides.forEach((slide, index) => {
                        const slidePosition = slide.offsetLeft - slider.offsetLeft;
                        const slideWidth = slide.offsetWidth;
                        if (scrollPosition >= slidePosition - slideWidth / 3 &&
                            scrollPosition < slidePosition + slideWidth / 3) {
                            dots.forEach(dot => dot.classList.remove('active'));
                            dots[index].classList.add('active');
                            currentSlide = index;
                        }
                    });
                });
            }
        });
    </script>
